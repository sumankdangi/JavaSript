// Addition
let val1 = 10+3;
console.log(`Addition: ${val1}`); // Output: Addition: 13
let val2 = 10 + -3;
console.log(`Addition: ${val2}`); // Output: Addition: 7

// Subtraction
let val3 = 10 - 3;
console.log(`Subtraction: ${val3}`); // Output: Subtraction: 7
let val4 = 10 - -3;
console.log(`Subtraction: ${val4}`); // Output: Subtraction: 13 

// Multiplication
let val5 = 10 * 3;
console.log(`Multiplication: ${val5}`); // Output: Multiplication: 30
let val6 = 10 * -3;
console.log(`Multiplication: ${val6}`); // Output: Multiplication: -30
// Division
let val7 = 10 / 2;
console.log(`Division: ${val7}`); // Output: Division: 5
let val8 = 10 / -2;
console.log(`Division: ${val8}`); // Output: Division: -5   

// Remainder
let val9 = 10 % 3;
console.log(`Remainder: ${val9}`); // Output: Remainder: 1
let val10 = -10 % 3;
console.log(`Remainder: ${val10}`); // Output: Remainder: - 1

// Exponentiation
let val11 = 2 ** 3;
console.log(`Exponentiation: ${val11}`); // Output: Exponentiation: 8
let val12 = 2 ** -3;
console.log(`Exponentiation: ${val12}`); // Output: Exponentiation: 0.125   

// Increment and Decrement
let val13 = 5;
++val13;
console.log(`Increment: ${val13}`); // Output: Increment: 6
--val13;
console.log(`Decrement: ${val13}`); // Output: Decrement: 5 
--val13;
console.log(`Decrement: ${val13}`); // Output: Decrement: 4




