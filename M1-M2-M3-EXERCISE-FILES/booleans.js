// Defining boolean 
let trueValue = true;
console.log(trueValue); // Output: true

let falseValue = false;
console.log(falseValue); // Output: false  


// Using the Not operator in boolean
let notTrue = !true;
console.log(notTrue); // Output: false
let notFalse = !false;
console.log(notFalse); // Output: true